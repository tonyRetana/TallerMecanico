    private void tblListaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblListaMouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount() == 2){
            LogicaCLlentes logica = new LogicaCLlentes();
            EntidadCliente cliente = new EntidadCliente();
            int filaSeleccionada = tblLista.getSelectedRow();
            if (filaSeleccionada != -1) {
                txtCedula.setText(tblLista.getValueAt(filaSeleccionada, 0).toString());
                try {
                    cliente = logica.ObtenerClientePorIdentificacion(Integer.parseInt(txtCedula.getText()));
                    txtNombre.setText(cliente.getNombre());
                    txtApellido1.setText(cliente.getPrimerApellido());
                    txtApellido2.setText(cliente.getSegundoApellido());
                    txtTelefono.setText(cliente.getTelefono());
                    txtCorreo.setText(cliente.getCorreoElectronico());
                    txtContrasena.setText(cliente.getContrasena());
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_tblListaMouseClicked


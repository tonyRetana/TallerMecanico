    public void LimpiarTabla(){
        modelo = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int colum){ return false; }
        };
        tblLista.setModel(modelo);
        modelo.addColumn("Cedula");
        modelo.addColumn("Nombre");
        modelo.addColumn("Primer");
        modelo.addColumn("Segundo");
        modelo.addColumn("Telefono");
        modelo.addColumn("Correo Electronico");
    }
    public void CargarClientes(String condicion)throws Exception{
        try {
            LogicaCLlentes logica = new LogicaCLlentes();
            List<EntidadCliente> arreglo;
            LimpiarTabla();
            Object[] fila = new Object[6];
            arreglo= logica.ListarClientes(condicion);
            for(EntidadCliente cli : arreglo){
                fila[0] = cli.getIdentificacion();
                fila[1] = cli.getNombre();
                fila[2] = cli.getPrimerApellido();
                fila[3] = cli.getSegundoApellido();
                fila[4] = cli.getTelefono();
                fila[5] = cli.getCorreoElectronico();
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            throw e;
        }
    }
    
    public void InsertarCliente(){
        int resultado = -1;
        try {
            LogicaCLlentes logica = new LogicaCLlentes();
            EntidadCliente cliente =  new EntidadCliente();
            cliente.setIdentificacion(Integer.parseInt(txtCedula.getText()));
            cliente.setNombre(txtNombre.getText());
            cliente.setPrimerApellido(txtApellido1.getText());
            cliente.setSegundoApellido(txtApellido2.getText());
            cliente.setCorreoElectronico(txtCorreo.getText());
            cliente.setTelefono(txtTelefono.getText());
            cliente.setContrasena(txtContrasena.getText());
            
            resultado = logica.InsertarCliente(cliente);
            JOptionPane.showMessageDialog(this, logica.getMensaje()+"\n"+cliente.MostrarPersona());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Agregue todos los datos correctamente");
        }
    }
    public void ActualizarCliente(){
        EntidadCliente cliente = new EntidadCliente();
        cliente.setContrasena(txtContrasena.getText());
        cliente.setCorreoElectronico(txtCorreo.getText());
        cliente.setIdentificacion(Integer.parseInt(txtCedula.getText()));
        cliente.setNombre(txtNombre.getText());
        cliente.setPrimerApellido(txtApellido1.getText());
        cliente.setSegundoApellido(txtApellido2.getText());
        cliente.setTelefono(txtTelefono.getText());
        
        try {
            LogicaCLlentes logica = new LogicaCLlentes();
            int resultado = logica.ActualizarCliente(cliente);
            JOptionPane.showMessageDialog(this, logica.getMensaje());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
